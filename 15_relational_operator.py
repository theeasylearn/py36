num1 = int(input("Enter first number"))
num2 = int(input("Enter second number"))

#check whether num1 and num2 has same value or not 
result = num1 == num2 
print(f"{result} = {num1} == {num2}")

#check whether num1 and num2 has different value
result = num1 != num2 
print(f"{result} = {num1} != {num2}")

#check whether num1 is bigger then num2 
result = num1 > num2 
print(f"{result} = {num1} > {num2}")

#check whether num1 smaller num2
result = num1 < num2 
print(f"{result} = {num1} < {num2}")

#check whether num1 is bigger then or equal to num2
result = num1 >= num2 
print(f"{result} = {num1} >= {num2}")

#check whether num1 lesser then or equal to num2 
result = num1 <= num2 
print(f"{result} = {num1} <= {num2}")
